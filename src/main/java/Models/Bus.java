package Models;

public class Bus {

    private String bus_no;
    private int seat;

    public String getBus_no() {
        return bus_no;
    }

    public int getSeat() {
        return seat;
    }

    public void setSeat(int seat) {
        this.seat = seat;
    }

    public void setBus_no(String bus_no) {
        this.bus_no = bus_no;
    }
}
